//varibel js itu ada tiga : var, let, const

var name1; //var jarang digunakan, mending pake let aja
name1 = "Billy";

console.log(name1);

let name2; //let itu nilainya bisa diubah ubah
name2 = "Billyy";
name2 = "joy"; //nanti outputnya gini
console.log(name2);

const name3 = "billyyy";
name3 = "onepiece"; //const itu nilainya harus di tetapkan dan tidak bisa diubah, ya karna konstan
console.log(name3);
